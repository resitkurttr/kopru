# ── Helper Functions ─────────────────────────────────────────────────────────

def get_provider_by_id(provider_id: str) -> Optional[Dict]:
    """ID ile provider getir. Bulunamazsa None döndür."""
    return PROVIDER_CATALOG.get(provider_id)


def list_providers_from_catalog(
    service_kind: Optional[str] = None,
    free_only: bool = False,
    category: Optional[str] = None,
) -> List[Dict]:
    """
    Katalogdan provider listesi getir.
    
    Args:
        service_kind: Servis türü filtresi (örn. "chat", "image", "code")
        free_only: True ise yalnızca ücretsiz planı olan sağlayıcıları getir
        category: Kategori filtresi (örn. "frontier", "gateway", "local")
    
    Returns:
        Filtrelenmiş provider listesi
    """
    results = []
    for pdata in PROVIDER_CATALOG.values():
        if free_only and not pdata.get("hasFree"):
            continue
        if service_kind and service_kind not in pdata.get("serviceKinds", []):
            continue
        if category and pdata.get("category") != category:
            continue
        results.append(pdata)
    return results


def get_providers_by_service_kind(kind: str) -> List[Dict]:
    """Belirli bir servis türünü destekleyen tüm provider'ları getir."""
    return list_providers_from_catalog(service_kind=kind)


def get_free_providers() -> List[Dict]:
    """Tüm ücretsiz planı olan provider'ları getir."""
    return list_providers_from_catalog(free_only=True)


def get_all_service_kinds() -> List[str]:
    """Katalogdaki tüm benzersiz servis türlerini listele."""
    kinds = set()
    for pdata in PROVIDER_CATALOG.values():
        kinds.update(pdata.get("serviceKinds", []))
    return sorted(kinds)


def get_all_categories() -> List[str]:
    """Katalogdaki tüm benzersiz kategorileri listele."""
    cats = set()
    for pdata in PROVIDER_CATALOG.values():
        cats.add(pdata.get("category", "unknown"))
    return sorted(cats)


def get_catalog_stats() -> Dict:
    """Katalog istatistiklerini getir."""
    total = len(PROVIDER_CATALOG)
    free_count = sum(1 for p in PROVIDER_CATALOG.values() if p.get("hasFree"))
    kinds_count = len(get_all_service_kinds())
    categories_count = len(get_all_categories())
    kind_distribution = {}
    category_distribution = {}
    for pdata in PROVIDER_CATALOG.values():
        for kind in pdata.get("serviceKinds", []):
            kind_distribution[kind] = kind_distribution.get(kind, 0) + 1
        cat = pdata.get("category", "unknown")
        category_distribution[cat] = category_distribution.get(cat, 0) + 1

    return {
        "total_providers": total,
        "free_providers": free_count,
        "paid_providers": total - free_count,
        "service_kinds_count": kinds_count,
        "categories_count": categories_count,
        "service_kind_distribution": kind_distribution,
        "category_distribution": category_distribution,
    }


